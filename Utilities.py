

class Calculator(object):

    def add(self, a: int, b: int) -> int:
        return a + b

    def mulf(self, a: int, b: int) -> int:
        return a * b

    def info(self):
        print('INFO')

    def minus(self, a: int, b: int) -> int:
        return a - b

    def minus2(self, a: int, b: int) -> int:
        return a - b
